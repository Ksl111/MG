## Формула факторизации (NWA)

В приближении узкой ширины (Narrow Width Approximation):

```
σ(pp → go go → X) ≈ σ(pp → go go) × BR(go → t t̄ G̃ γ)²
```

Это работает, потому что пропагатор резонанса:

```
         1                      π
─────────────────────  →  ──────────── δ(p² - M²)
(p² - M²)² + M²Γ²        M × Γ
```

при Γ → 0 становится дельта-функцией, что физически означает,
что глюино живёт достаточно долго, чтобы «забыть» как оно было рождено.

