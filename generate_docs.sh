## Рекомендация

**Используйте раздельный подход** (`run_production_decay.mg5`):
- Быстро: минуты вместо часов/дней
- Достаточно точно для большинства SUSY-сценариев
- MadSpin корректно учитывает спин-корреляции

Каскадный подход (`run_cascade.mg5`) используйте только для:
- Валидации результатов раздельного подхода
- Исследования эффектов конечной ширины
- Тестов на малом числе событий (benchmark_timing.sh)
