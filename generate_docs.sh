## Типичные паттерны потребления ресурсов

### Production (p p > go go)
- CPU: 100% на одном ядре
- RAM: 100–500 MB
- Длительность: минуты

### Cascade (p p > go go, go > t t~ grv a)
- CPU: 100% на одном ядре (генерация), затем ~100% × N ядер (интеграция)
- RAM: 1–10+ GB (зависит от числа диаграмм)
- Длительность: часы–дни
- Disk I/O: высокий при компиляции Fortran-кода
