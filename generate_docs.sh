## Детальное объяснение

### 1. Число Фейнмановских диаграмм

#### Раздельный подход: `p p → go go`
В этом процессе мы считаем только **рождение пары глюино**:
- `q q̄ → go go` через s-канальный глюон
- `g g → go go` через s-, t-, u-каналы и 4-точечную вершину

Типичное число диаграмм: **~10** (зависит от модели).

Распады `go → t t̄ G̃ γ` считаются **отдельно** через MadSpin, который:
- Для каждого распада генерирует диаграммы 1 → 4
- Вычисляет матричный элемент распада независимо
- Сохраняет спин-корреляции в приближении узкой ширины (NWA)

#### Каскадный подход: `p p → go go, go → t t̄ G̃ γ`
Здесь MadGraph вычисляет **полный матричный элемент** процесса:
```
p p → t t̄ G̃ γ  t t̄ G̃ γ
```
Это процесс **2 → 10** (10 частиц в конечном состоянии!). Число Фейнмановских
диаграмм растёт **факториально** с числом конечных частиц:

- Для 2 → 2: ~10 диаграмм
- Для 2 → 4: ~100 диаграмм
- Для 2 → 6: ~1000 диаграмм
- Для 2 → 8: ~10000 диаграмм
- Для 2 → 10: ~100000+ диаграмм

### 2. Фазовое пространство

#### Раздельный подход
Интегрирование фазового пространства:
- **Production**: 2-частичное ф.п. → dim = 2 (2×3 - 4 - 2 = 2)
- **Decay**: 4-частичное ф.п. → dim = 8 (4×3 - 4 = 8) × 2 распада

Общая размерность **не** складывается, потому что интегрирование проводится
отдельно: сначала production, потом decay.

#### Каскадный подход
Интегрирование фазового пространства:
- **Полный процесс**: 10-частичное ф.п.
- dim = 3×10 - 4 = **26** переменных интегрирования

Интегрирование по 26-мерному пространству с помощью Монте-Карло требует
**экспоненциально** больше точек для достижения той же точности.

### 3. Вычисление матричного элемента

Время вычисления одного значения |M|² пропорционально числу диаграмм.
Для каскадного процесса:

- Нужно вычислить **все** интерференционные члены между диаграммами
- Число интерференционных членов ~ N² (где N — число диаграмм)
- При N ~ 10000 диаграмм → ~10⁸ интерференционных членов

Для раздельного подхода:
- Production: ~10 диаграмм → ~100 интерференционных членов
- Каждый decay: отдельный расчёт, гораздо меньше диаграмм

### 4. Компиляция кода

MadGraph генерирует **Fortran-код** для вычисления матричных элементов.

- Раздельный: компилируется маленький код для 2 → 2
- Каскадный: компилируется огромный код для 2 → 10
  - Fortran-файлы могут занимать сотни МБ
  - Компиляция может занимать часы
  - Возможна нехватка памяти при компиляции

### 5. Интеграция (Monte Carlo)

Для численного интегрирования сечения MadGraph использует
алгоритм VEGAS (адаптивный Monte Carlo).

| | Раздельный | Каскадный |
|---|---|---|
| Размерность | 2D | 26D |
| Число точек на итерацию | ~10³ | ~10⁵–10⁶ |
| Число итераций | ~5–10 | ~20–50+ |
| Время одной точки | ~0.01 мс | ~10–100 мс |
| **Итого** | **секунды** | **часы–дни** |

### 6. Когда нужен каскадный подход?

Каскадный подход необходим, если:

1. **Ширина частицы сравнима с её массой** (Γ/M > 1%) — нарушение
   приближения узкой ширины (NWA).
2. **Важны интерференции между production и decay** — например,
   когда одинаковые конечные состояния могут возникать через разные
   промежуточные частицы.
3. **Нужны точные спин-корреляции** на уровне полного матричного элемента.

Для глюино с типичными SUSY-параметрами:
- Ширина Γ(go) << M(go), поэтому **NWA работает хорошо**
- Раздельный расчёт даёт точность ~O(Γ/M), что обычно < 1%
- **Рекомендуется раздельный подход** (production + MadSpin)

